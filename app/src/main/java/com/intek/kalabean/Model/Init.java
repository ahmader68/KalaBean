package com.intek.kalabean.Model;

public class Init {
    private String apkExpire;
    private String company;
    private String align;
    private String dir;
    private String currency;
    private String lang;
    private boolean singleLang;
    private String apkVersion;
    private String apkUrl;

    public String getApkExpire() {
        return apkExpire;
    }

    public void setApkExpire(String apkExpire) {
        this.apkExpire = apkExpire;
    }

    public String getCompany() {
        return company;
    }

    public void setCompany(String company) {
        this.company = company;
    }

    public String getAlign() {
        return align;
    }

    public void setAlign(String align) {
        this.align = align;
    }

    public String getDir() {
        return dir;
    }

    public void setDir(String dir) {
        this.dir = dir;
    }

    public String getCurrency() {
        return currency;
    }

    public void setCurrency(String currency) {
        this.currency = currency;
    }

    public String getLang() {
        return lang;
    }

    public void setLang(String lang) {
        this.lang = lang;
    }

    public boolean isSingleLang() {
        return singleLang;
    }

    public void setSingleLang(boolean singleLang) {
        this.singleLang = singleLang;
    }

    public String getApkVersion() {
        return apkVersion;
    }

    public void setApkVersion(String apkVersion) {
        this.apkVersion = apkVersion;
    }

    public String getApkUrl() {
        return apkUrl;
    }

    public void setApkUrl(String apkUrl) {
        this.apkUrl = apkUrl;
    }
}
